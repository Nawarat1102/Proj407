st.markdown("""
1. วัตถุประสงค์
โปรแกรมนี้ถูกพัฒนาเพื่อช่วยผู้ใช้คำนวณค่ารากของสมการด้วยวิธี **False Position Method**  
- ใช้สำหรับหาค่ารากของสมการในช่วงที่กำหนด  
- แสดงผลเป็นตารางขั้นตอน และกราฟแสดงความคืบหน้า

---

2. วิธีใช้งาน (Step-by-step)
    1. ป้อนสมการที่ต้องการคำนวณ (เช่น `x**3 - x - 2`)  
    2. กำหนดค่าช่วง `[a, b]` ที่ต้องการประมาณราก  
    3. ตั้งค่า **Tolerance** (ความแม่นยำ) เช่น 0.001  
    4. กดปุ่ม **คำนวณ**  
    5. โปรแกรมจะแสดงผลลัพธ์ในตาราง และกราฟแสดงค่าประมาณราก `c` ในแต่ละรอบ

-----------------------------------------------------------------------------------------

3. คำอธิบายผลลัพธ์
- **a, b** : ช่วงที่ใช้ในการประมาณราก  
- **c** : ค่าประมาณรากของสมการในแต่ละรอบ  
- **f(c)** : ค่าฟังก์ชันที่จุด c  
- **กราฟ** : แสดงค่าประมาณราก `c` แต่ละรอบเพื่อดูความคืบหน้า

---

4. ข้อควรระวัง / ข้อจำกัด
- ช่วง `[a, b]` ต้องมีรากจริงของสมการ  
- หาก f(a) * f(b) > 0 โปรแกรมจะไม่สามารถคำนวณได้  
- ความแม่นยำขึ้นอยู่กับค่า **Tolerance** ที่ตั้งไว้  

---

5. ตัวอย่างการใช้งาน
- สมการ: `x**3 - x - 2 = 0`  
- ช่วง: `[1, 2]`  
- Tolerance: `0.001`  
- โปรแกรมจะแสดงตารางขั้นตอน และกราฟค่า `c` แต่ละรอบ

---

6. คำถามที่พบบ่อย (FAQ)
- **ทำไมกราฟไม่ขึ้น?**  
  ตรวจสอบว่ามีการคำนวณแล้วหรือยัง และค่า `c` ถูกบันทึกครบทุกขั้นตอน  
- **ค่ารากไม่เปลี่ยน?**  
  อาจเกิดจากค่าช่วง `[a, b]` ไม่ถูกต้อง หรือสมการไม่มีรากจริงในช่วงนั้น

""")
